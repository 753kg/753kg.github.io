package openTutorials;

public class OutputMethod {

	public static String a() {
		// ...
		return "a";		// return에서 메서드가 끝남.
	}
	
	public static int one() {
		return 1;
	}
	
	public static void main(String[] args) {
		// 생활코딩 JAVA - Method
		// 메소드의 출력
		
		System.out.println(a());
		System.out.println(one());

	}

}
