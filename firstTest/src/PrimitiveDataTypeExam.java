
public class PrimitiveDataTypeExam {

	public static void main(String[] args) {
		// 자바 입문 파트2 기본형 타입
		// char는 '' 작은따옴표
		// 59같은, 값 자체를 '리터럴' 이라 함.
		// long 타입은 값 뒤에 l 또는 L을 붙어야 함.
		// float 타입은 값 뒤에 f 붙이기.
		
		boolean isFun = true;
		System.out.println(isFun);
		
		char c = 'f';
		
		int x = 59;
		
		long bing = 3454564l;
		
		float f = 32.4f;
		
		double d = 343434.5;
	}

}
